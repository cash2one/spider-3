//获取二级导航
function Dh_GetNav(ID)
{
    var t_Box="";
    var g_conType=eval('JmwConvType_'+ID);
    var g_conType_Num=0;
    try{
        g_conType_Num=eval('JmwConvType_h_'+ID);
    }catch(err){}

    if(g_conType!=null && g_conType!='')
    {
        for(var j=0;j<g_conType.length;j++)
        {
            //第一行业加粗体
            var titleStr = g_conType[j][0].replace(/<span>/,'');
            titleStr = titleStr.replace(/<\/span>/,'');
            if(j==0)
            {
                t_Box+="<a href=\""+g_conType[j][1]+"\" title=\""+titleStr+"加盟\" target=\"_blank\"><b>"+g_conType[j][0]+"</b></a>";
            }else
            {
                t_Box+="<a href=\""+g_conType[j][1]+"\" title=\""+titleStr+"加盟\" target=\"_blank\">"+g_conType[j][0]+"</a>";
            }
            if(g_conType_Num!=0 && g_conType_Num==j)
            {
                t_Box+="<br />";
            }else
            {
                t_Box+="|";
            }
        }
        t_Box=t_Box.substring(0,t_Box.length-1);
    }
    return t_Box;
}