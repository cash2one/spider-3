                                                    <p>　　土巴兔致力于让天下没有烦心的装修。为实现这个使命，全体土巴兔人不断努力，推动装修、建材、家居市场的规范化，改变着数千万家庭的装修方式和置家理念，为用户创造卓越的互联网装修体验。作为互联网装修企业，土巴兔将通过O2O的模式，为传统装修及家居领域带来新的变革，不断为用户创造价值，推动行业发展。</p>
